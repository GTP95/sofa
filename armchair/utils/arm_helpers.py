def return_instruction_type_arm(instruction):
    """
    Determines the type of an ARMv4 instruction based on predefined categories.

    Args:
        instruction (str): The mnemonic of an ARMv4 instruction (e.g., "LDR", "STR").

    Returns:
        str: The type of the instruction, such as "LOAD", "STORE", "LOGICAL", etc.
        If the instruction is not found in any category, it returns "Unknown".
    """
    type_instruction = "Unknown"

    if instruction in ARMv4["LOAD"]:
        type_instruction = "LOAD"
    elif instruction in ARMv4["STORE"]:
        type_instruction = "STORE"
    elif instruction in ARMv4["LOGICAL"]:
        type_instruction = "LOGICAL"
    elif instruction in ARMv4["ARITHMETIC"]:
        type_instruction = "ARITHMETIC"
    elif instruction in ARMv4["SHIFT"]:
        type_instruction = "SHIFT"
    elif instruction in ARMv4["BRANCH"]:
        type_instruction = "BRANCH"
    elif instruction in ARMv4["COMPARE"]:
        type_instruction = "COMPARE"
    elif instruction in ARMv4["STACK"]:
        type_instruction = "STACK"
    elif instruction in ARMv4["MULTIPLE_LOAD_STORE"]:
        type_instruction = "MULTIPLE_LOAD_STORE"
    elif instruction in ARMv4["MISCELLANEOUS"]:
        type_instruction = "MISC"
    elif instruction in ARMv4["NO_OP"]:
        type_instruction = "NO OPERATION"

    return type_instruction


